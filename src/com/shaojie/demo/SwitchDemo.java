package com.shaojie.demo;

public class SwitchDemo {
    public static void main(String[] args) {
        int type = 4;
        switch (type) {
            default:
                System.out.println(4);
            case 1:
                System.out.println(1);
            case 2:
                System.out.println(2);
            case 3:
                System.out.println(3);
        }
    }
}
